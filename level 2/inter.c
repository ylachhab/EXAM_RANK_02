#include <unistd.h>

int main(int ac, char **av)
{
	int i = 0;
	int j = 0;

	if (ac == 3)
	{
		while (av[1][i])
		{
			if (av[2][j] == av[1][i])
				write(1, &av[1][i], 1);
			if (av[1][i] != av[2][j])
				i++;
			j++;
			i++;
		}
	}
	write(1, "\n", 1);
}
