#include <unistd.h>
#include <stdio.h>

int main(int ac, char **av)
{
	int i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if ((av[1][0] >= 'a' && av[1][0] <= 'z'))
			{
					av[1][0] -= 32;
					write(1, &av[1][0], 1);
					i++;
			}
			if (av[1][i] == ' ' && (av[1][i + 1] >= 'a' && av[1][i + 1] <= 'z'))
			{
					av[1][i + 1] -= 32;
					write(1, &av[1][i], 1);
					i++;
			}
			else if (av[1][i - 1] != ' ' && (av[1][i] >= 'A' && av[1][i] <= 'Z'))
				av[1][i] += 32;
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write (1, "\n", 1);
}